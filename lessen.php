						<h3>Bijlessen</h3>
						Studenten en scholieren kunnen bijlessen krijgen. Deze lessen duren een uur of langer afhankelijk van de behoefte. Je kunt individueel les krijgen maar het is ook mogelijk om met meerdere studenten/scholieren tegelijk les te nemen. Doel van deze bijlessen is om je klaar te stomen voor het tentamen of om je Spaans op een hoger niveau te krijgen.
						<hr>
						<h3>Vakantiecursus</h3>
